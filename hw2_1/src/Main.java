public class Main {
    public static void main(String[] args) {
        int sum = 0;
        for (int j = 1; j < 10; j++){
            for (int i = 1; i < 10; i++){
                sum = i+j;
                System.out.print(i + " + " + j + " = " + sum + "\t");
            }
        System.out.println();
        }


    }
}